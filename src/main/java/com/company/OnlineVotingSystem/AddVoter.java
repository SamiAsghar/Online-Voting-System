    private void jtxtNameKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtxtNameKeyPressed
        // TODO add your handling code here:
        
        char c = evt.getKeyChar();
        
        if(Character.isLetter(c) || Character.isWhitespace(c) || Character.isISOControl(c) || evt.getKeyCode()== KeyEvent.VK_SHIFT ){
            jtxtName.setEditable(true);
        }
        else{
            jtxtName.setEditable(false);
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(null, "Only alphabets are allowed." , "Error", JOptionPane.PLAIN_MESSAGE);
        }
    }//GEN-LAST:event_jtxtNameKeyPressed

