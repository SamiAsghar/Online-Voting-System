    private void jbtnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnAddActionPerformed
       
       if(!(jtxtName.getText().trim().equals("") || jtxtFName.getText().trim().equals("") || jtxtCnic.getText().trim().equals("") 
                 || jtxtAddress.getText().trim().equals("")))
         {
             if((jDCDob.getDate() == null)){
             JOptionPane.showMessageDialog(null, "Select Date of Birth." , "Error", JOptionPane.PLAIN_MESSAGE);
             this.setVisible(true);
             }
             else{
                int cniclength = jtxtCnic.getText().length(); 
             if(cniclength<=12)
             {
             JOptionPane.showMessageDialog(null, "Cnic should have 13 digits." , "Error", JOptionPane.PLAIN_MESSAGE);
             this.setVisible(true);
             }
             else
             {
              if(!jComboGender.getSelectedItem().equals("Select Gender"))
              {
                  DateFormat fmt = new SimpleDateFormat("yyyy-MM-dd");
         String date1 = fmt.format(jDCDob.getDate());
         int birthyear = Integer.parseInt(date1.substring(0, 4));
         int birthmonth = Integer.parseInt(date1.substring(5, 7));
         int birthdate = Integer.parseInt(date1.substring(8, 10));
         
         LocalDate dateofbirth = LocalDate.of(birthyear,birthmonth, birthdate);
         LocalDate todayDate = LocalDate.now();
         
         Period diff = Period.between(dateofbirth, todayDate);
         String calculate = String.format("%02d", diff.getYears());
         int calculateAge = Integer.parseInt(calculate);
         
                if(calculateAge>=18)
                {
                  
           Name.add(jtxtName.getText());
           FName.add(jtxtFName.getText());
           Cnic.add(jtxtCnic.getText());
           Gender.add(jComboGender.getSelectedItem().toString());
           Dob.add(date1);
           Age.add(calculate);
           Address.add(jtxtAddress.getText());
           VoterId.add((Vid+i));
                  
            this.setVisible(false);    
            JOptionPane.showMessageDialog(null, "Voter has been added.\nVoter ID: "+VoterId.get(i)+"\nVoter Name: "+Name.get(i) , "Message", JOptionPane.PLAIN_MESSAGE);
    
            backAdmin.setVisible(true);
            i++;
                }
                
                else
                {
                    Toolkit.getDefaultToolkit().beep();
                 JOptionPane.showMessageDialog(null, "Entered age is not legal. Age should be 18 or above." , "Message", JOptionPane.PLAIN_MESSAGE);
                 this.setVisible(true);
                
                }
              }
              else
              {
              JOptionPane.showMessageDialog(null, "Select Gender." , "Message", JOptionPane.PLAIN_MESSAGE);
                 this.setVisible(true);
              }
                  
             }
         }
          }
         
         
       else
       {
           JOptionPane.showMessageDialog(null, "Fill all the fields" , "Message", JOptionPane.PLAIN_MESSAGE);
       this.setVisible(true);
       }
     
     
      
    }//GEN-LAST:event_jbtnAddActionPerformed

