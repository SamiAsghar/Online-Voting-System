    private void jtxtCnicKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtxtCnicKeyPressed
        // TODO add your handling code here:
        int cniclength = jtxtCnic.getText().length();
        char c = evt.getKeyChar();
        if(Character.isDigit(c) || Character.isWhitespace(c) || Character.isISOControl(c)){
            jtxtCnic.setEditable(true);
        }
        else{
            Toolkit.getDefaultToolkit().beep();
            jtxtCnic.setEditable(false);
            JOptionPane.showMessageDialog(null, "Only integers are allowed." , "Error", JOptionPane.PLAIN_MESSAGE);
        }
        if(cniclength>12){
            if(Character.isISOControl(c))
            {
            jtxtCnic.setEditable(true);
            }
            else
            jtxtCnic.setEditable(false);
        }

    }//GEN-LAST:event_jtxtCnicKeyPressed

